{
    for (int i = 0; i < 64; i++) mask[i] = 0xFF;
    // Xbox data pointers: any 4-byte value ending in 0x03 0x00 (points to 0x00XXXX03XX range)
    for (int i = 0; i <= 64 - 4; i++) {
        if (pat[i+2] == 0x03 && pat[i+3] == 0x00) {
            mask[i] = mask[i+1] = mask[i+2] = mask[i+3] = 0x00;
        }
    }
    // Indirect call/jmp through absolute address: FF 15 XX XX XX XX or FF 25 XX XX XX XX
    for (int i = 0; i <= 64 - 6; i++) {
        if (pat[i] == 0xFF && (pat[i+1] == 0x15 || pat[i+1] == 0x25)) {
            mask[i+2] = mask[i+3] = mask[i+4] = mask[i+5] = 0x00;
        }
    }
}

static uint32_t find_pattern(const uint8_t* pat, uint32_t start, uint32_t end)
{
    uint8_t mask[64];
    build_mask(pat, mask);
    for (uint32_t addr = start; addr <= end - 64; addr++) {
        uint8_t* b = (uint8_t*)addr;
        int match = 1;
        for (int i = 0; i < 64; i++) {
            if (mask[i] && b[i] != pat[i]) { match = 0; break; }
        }
        if (match) return addr;
    }
    return 0;
}

static void patch_pattern(const char* name, const uint8_t* pat, void* fn)
{
    uint32_t addr = find_pattern(pat, 0x00011000, 0x00100000);
    if (addr) {
        printf("Found %s @ 0x%08X\n", name, addr);
        patch_function(addr, fn);
    } else {
        printf("NOT FOUND: %s\n", name);
    }
}

